    private void sortTableByPrice(boolean ascending) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowCount = model.getRowCount();

        // Verileri geçici listeye al
        List<Product> sortedList = new ArrayList<>(displayedProducts);

        // Fiyatlara göre sırala
        sortedList.sort(new Comparator<Product>() {
            public int compare(Product p1, Product p2) {
                return ascending ? Double.compare(p1.getPrice(), p2.getPrice())
                        : Double.compare(p2.getPrice(), p1.getPrice());
            }
        });

        // Tabloyu temizle ve yeniden doldur
        model.setRowCount(0);
        for (Product p : sortedList) {
            model.addRow(new Object[]{p.getProduct(), p.getPrice()});
        }

        // displayedProducts listesini güncelle
        displayedProducts.clear();
        displayedProducts.addAll(sortedList);

    }

    private void filterByName() {
        String searchText = jTextField1.getText().trim().toLowerCase();
        defTab.setRowCount(0); // Tabloyu temizle
        displayedProducts.clear(); // Gösterilenleri sıfırla

        for (Product p : Product.getProductList()) {
            if (searchText.isEmpty() || p.getProduct().toLowerCase().startsWith(searchText)) {
                Object[] row = {p.getProduct(), p.getPrice()};
                defTab.addRow(row);
                displayedProducts.add(p); // Eşleşenleri ekle
            }
        }
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedItem = jTable1.getSelectedRow();

        if (selectedItem >= 0) {
            Product selectedProduct = displayedProducts.get(selectedItem);

            //  Stok kontrolü
            if (selectedProduct.getQuantity() <= 0) {
                JOptionPane.showMessageDialog(null, "This product is out of stock!");
                return;
            }

            //  Stoktan 1 düş
            selectedProduct = new Product(
                    selectedProduct.getProduct(),
                    selectedProduct.getPrice(),
                    selectedProduct.getQuantity() - 1
            );

            // Güncel veriyi veritabanına kaydet
            boolean updateSuccess = ProductDAO.updateProduct(selectedProduct);
            if (!updateSuccess) {
                JOptionPane.showMessageDialog(null, "Error while updating product stock.");
                return;
            }

            // Sepete ekle
            MainScreen.getCartList().add(selectedProduct);
            JOptionPane.showMessageDialog(null, "Product added to cart.");

            //  Log
            String email = (User.currentUser != null) ? User.currentUser.getEmail() : "unknown";
            String logEntry = "Product added to cart: " + selectedProduct.getProduct() + ", Price: " + selectedProduct.getPrice() + " by: " + email;
            Transaction.writeToFile("[" + new java.util.Date() + "] " + logEntry);

            //  İndirimli toplam hesapla
            double total = 0.0;
            for (Product p : MainScreen.getCartList()) {
                total += p.getPrice();
            }
            double discountedTotal = total * 0.9;

            Transaction.writeDiscountedTotalToFile(discountedTotal);

            //  Arayüzü güncellemek istersen tabloyu yeniden yükle:
            Product.loadFromDB();
            loadToTable();

        } else {
            JOptionPane.showMessageDialog(null, "Please choose a product.");
        }

        jTable1.clearSelection();
    }//GEN-LAST:event_jButton1ActionPerformed

