    public static void loadToTable() {
        defTab.setRowCount(0);
        displayedProducts.clear(); // Tabloya basılanları sıfırla

        for (Product p : Product.getProductList()) {
            Object[] row = {p.getProduct(), p.getPrice()};
            defTab.addRow(row);
            displayedProducts.add(p); // Şu an gösterilen ürünü ekle
        }
    }

    public static void removeFromTable(Product productToRemove) {
        // displayedProducts içinden kaldır
        displayedProducts.remove(productToRemove);

        // Product listeden de kaldırmak istiyorsan (veritabanından değilse)
        Product.getProductList().remove(productToRemove);

        // Tabloyu güncelle
        loadToTable();
    }

    public static void loadToTableAfterAdd(Product newProduct) {
        Product.addProduct(newProduct); // Listeye ekle
        Object[] row = {newProduct.getProduct(), newProduct.getPrice()};
        defTab.addRow(row);
        displayedProducts.add(newProduct);
    }

