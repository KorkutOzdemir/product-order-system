    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow >= 0) {
            Product removedProduct = MainScreen.getCartList().get(selectedRow);

            // Veritabanında stok miktarını 1 artır
            try {
                Connection conn = DBConnection.getConnection();
                PreparedStatement pst = conn.prepareStatement("UPDATE products SET quantity = quantity + 1 WHERE name = ?");
                pst.setString(1, removedProduct.getProduct());
                pst.executeUpdate();
                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }

            // Ürünü sepetten çıkar
            MainScreen.getCartList().remove(selectedRow);
            defTab.removeRow(selectedRow);

            // Sepet değiştiği için indirim iptal ediliyor
            discountApplied = false;
            if (User.currentUser != null) {
                User.currentUser.setDiscountApplied(false);
            }
            updateTotalPrice();

            // Log yaz
            String email = (User.currentUser != null) ? User.currentUser.getEmail() : "unknown";
            String logEntry = "Product removed from cart: " + removedProduct.getProduct() + ", Price: " + removedProduct.getPrice() + " by: " + email;
            Transaction.writeToFile("[" + new java.util.Date() + "] " + logEntry);

        } else {
            JOptionPane.showMessageDialog(this, "Please choose a product..");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

