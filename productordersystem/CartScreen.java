    private void applyDiscountSilently() {
        if (User.currentUser != null && User.currentUser.isDiscountApplied()) {
            int sum = 0;
            for (Product p : MainScreen.getCartList()) {
                sum += p.getPrice();
            }
            int discountedPrice = (int) (sum * 0.9); // %10 indirim
            jLabel3.setText(discountedPrice + "");
            discountApplied = true;
        }
    }

    public void loadToTable() {
        defTab.setRowCount(0);
        for (Product p : MainScreen.getCartList()) {
            Object[] row = {p.getProduct(), p.getPrice()};
            defTab.addRow(row);
        }
        discountApplied = false; // Sepet değişti, indirim iptal
        updateTotalPrice();
        
        discountApplied = false;
        if (User.currentUser != null) {
            User.currentUser.setDiscountApplied(false); // Kullanıcıdaki indirim durumu sıfırla
        }
    }

    public void updateTotalPrice() {
        int sum = 0;
        for (Product p : MainScreen.getCartList()) {
            sum += p.getPrice();
        }
        jLabel3.setText(sum + "");
    }

    public void applyDiscountCode() {
        if (discountApplied || (User.currentUser != null && User.currentUser.isDiscountApplied())) {
            JOptionPane.showMessageDialog(this, "Discount code already applied!");
            return;
        }

        String code = jTextField1.getText().trim();

        if (DISCOUNT_CODE.equalsIgnoreCase(code)) {
            int sum = 0;
            for (Product p : MainScreen.getCartList()) {
                sum += p.getPrice();
            }
            int discountedPrice = (int) (sum * 0.9); // %10 indirim
            jLabel3.setText(discountedPrice + "");
            discountApplied = true;

            if (User.currentUser != null) {
                User.currentUser.setDiscountApplied(true); // kullanıcıya uygula
            }

            JOptionPane.showMessageDialog(this, "Discount code applied successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Invalid discount code!");
        }
    }

