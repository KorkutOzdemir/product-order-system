    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String email = jTextField1.getText();
            String password = new String(jPasswordField1.getPassword());

            //Email control..
            if (!email.endsWith("@gmail.com") && !email.endsWith("@hotmail.com")) {
                JOptionPane.showMessageDialog(null, "Invalid email..try again..");
                return;
            }

            //Checking is password contains number..
            boolean hasDigit = false;
            for (char c : password.toCharArray()) {
                if (Character.isDigit(c)) {
                    hasDigit = true;
                    break;
                }
            }

            if (!hasDigit) {
                JOptionPane.showMessageDialog(null, "Password must contain a number..");
                return;
            }

            if (hasDigit) {
                if (new String(jPasswordField1.getPassword()).equals(new String((jPasswordField2.getPassword())))) {
                    User user = new User(email, password);
                    User.addUser(user);
                    writeUserToFile(user);
                    insertUserToDatabase(user); // <-- yeni satÄ±r
                    JOptionPane.showMessageDialog(null, "Sign up successfully..!");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Your passwords do not match!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error.." + e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

